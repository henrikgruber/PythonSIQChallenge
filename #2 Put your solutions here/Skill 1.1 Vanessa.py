# This program is collecting 3 nummers from the user and returns the sum

print("Enter the first number")
a = int(input())

print("Enter the second number")
b = int(input())

print("Enter the third number")
c = int(input())

s = int(a+b+c)

print("This is the sum of the three entered numbers:")
print(a, "+", b, "+", c, "=", s)
